    ALTER COLUMN column_id SET NOT NULL,
    ALTER COLUMN position SET NOT NULL;

ALTER TABLE issues
    ADD CONSTRAINT fk_issues_column_project
        FOREIGN KEY (column_id, project_id) REFERENCES board_columns (id, project_id);

